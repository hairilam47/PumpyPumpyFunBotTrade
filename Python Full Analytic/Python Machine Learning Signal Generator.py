    async def load_models(self):
        """Load trained models from disk."""
        import joblib
        
        model_dir = Path("models")
        
        if not model_dir.exists():
            logger.warning("No saved models found")
            return False
        
        try:
            # Load sklearn models
            self.models['price_predictor'] = joblib.load(model_dir / "price_predictor.pkl")
            self.models['price_regressor'] = joblib.load(model_dir / "price_regressor.pkl")
            self.models['rugpull_detector'] = joblib.load(model_dir / "rugpull_detector.pkl")
            
            # Load scalers
            self.scalers = joblib.load(model_dir / "scalers.pkl")
            
            # Load feature importance
            with open(model_dir / "feature_importance.json", "r") as f:
                self.feature_importance = json.load(f)
            
            # Load PyTorch model
            self.models['deep_predictor'].load_state_dict(
                torch.load(model_dir / "deep_predictor.pt")
            )
            
            self.is_trained = True
            logger.info("Models loaded from disk")
            return True
            
        except Exception as e:
            logger.error(f"Error loading models: {e}")
            return False